			<p>In 2010, a student did not come back to the institute after 2nd semester. When Dr. Manoj Pradhan (85-Mining), Associate Professor, NIT, came to know of such a happening, he made enquiries only to find out that the student’s family simply do not have any money to support his education and the student is working as daily wage labourer. Dr. Pradhan coordinated with student’s classmates to call him back and arranged for necessary funds from few alumni to ensure his continuing education, thus laying the foundation of Student Educational Assistance Fund.</p>
